package entites;


public class Auteur {
    private String prenom;
    private String nom;
    private String id;

    public Auteur(String prenom, String nom, String id) {
        this.prenom = prenom;
        this.nom = nom;
        this.id = id;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    
    
    
}
